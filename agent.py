# ---------------------------
# Form
# ---------------------------

with st.form("email_form"):

    sender_email = st.text_input("Sender Email")
    sender_password = st.text_input("App Password", type="password")
    recipient = st.text_input("Recipient Email")

    purpose = st.text_area("What is the email about?")

    # 📄 Context PDF (for AI only)
    st.markdown("### 📄 Extra Context for AI (Optional)")
    context_pdf = st.file_uploader(
        "Upload PDF (used only for generating better email)",
        type=["pdf"],
        key="context_pdf"
    )

    # 📎 Attachment PDF (for sending)
    st.markdown("### 📎 Attachment (Optional)")
    attachment_pdf = st.file_uploader(
        "Upload PDF to attach with email",
        type=["pdf"],
        key="attachment_pdf"
    )

    submit = st.form_submit_button("Generate Email")


# ---------------------------
# Generate Email
# ---------------------------

if submit:

    if not valid_email(sender_email) or not valid_email(recipient):
        st.error("❌ Invalid email format")

    else:
        pdf_text = ""

        # Only use context PDF for LLM
        if context_pdf:
            pdf_text = extract_pdf_text(context_pdf)

        prompt = f"""
You are an email assistant.

User request:
{purpose}

Extra context:
{pdf_text[:2000]}

You are a professional email assistant.

Write a clear and human sounding email based on the user's request.

Important instructions:
- Do not use any special characters such as dashes, asterisks, emojis, or symbols.
- Do not use bullet points or decorative formatting.
- Write in simple, natural, human language.
- Keep the tone professional, polite, and realistic.
- Avoid robotic or overly formal phrases.
- Do not add unnecessary formatting or spacing.

The email should feel like it was written by a real person.

User request:
{purpose}

Return strictly in this format:

Subject: <subject>

Body:
<body>

"""

        response = llm.invoke(prompt)
        text = response.content

        subject = ""
        body = ""

        if "Subject:" in text and "Body:" in text:
            subject = text.split("Subject:")[1].split("Body:")[0].strip()
            body = text.split("Body:")[1].strip()

        # Save to session
        st.session_state.subject = subject
        st.session_state.body = body
        st.session_state.sender_email = sender_email
        st.session_state.sender_password = sender_password
        st.session_state.recipient = recipient
        st.session_state.attachment = attachment_pdf


# ---------------------------
# Show Output
# ---------------------------

