import streamlit as st
from langchain_groq import ChatGroq
import smtplib
from email.message import EmailMessage
import re
from dotenv import load_dotenv
from PyPDF2 import PdfReader
import os
# ---------------------------
# Load ENV
# ---------------------------
load_dotenv()

# ---------------------------
# Initialize LLM
# ---------------------------
api_key = os.getenv("GROQ_API_KEY") or st.secrets["GROQ_API_KEY"]
sender_email = os.getenv("EMAIL_USER") or st.secrets["EMAIL_USER"]
sender_password = os.getenv("EMAIL_PASS") or st.secrets["EMAIL_PASS"]

llm = ChatGroq(
    model="qwen/qwen3-32b",
    temperature=0,
    api_key=api_key,
)

# ---------------------------
# Helpers
# ---------------------------

def valid_email(email):
    pattern = r"^[\w\.-]+@[\w\.-]+\.\w+$"
    return re.match(pattern, email)


def extract_pdf_text(file):
    text = ""
    try:
        reader = PdfReader(file)
        for page in reader.pages:
            text += page.extract_text() or ""
    except:
        pass
    return text


def send_email(sender_email, sender_password, recipient, subject, body, attachment=None):

    msg = EmailMessage()
    msg["From"] = sender_email
    msg["To"] = recipient
    msg["Subject"] = subject
    msg.set_content(body)

    # 📎 Attach only attachment PDF (NOT context PDF)
    if attachment is not None:
        file_data = attachment.getvalue()
        file_name = attachment.name

        msg.add_attachment(
            file_data,
            maintype="application",
            subtype="pdf",
            filename=file_name
        )

    try:
        with smtplib.SMTP_SSL("smtp.gmail.com", 465) as smtp:
            smtp.login(sender_email, sender_password)
            smtp.send_message(msg)

        return "✅ Email sent successfully!"

    except smtplib.SMTPAuthenticationError:
        return "❌ Authentication failed."

    except Exception as e:
        return f"❌ Failed: {str(e)}"


